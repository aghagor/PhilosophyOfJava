public class Autoboxing {
    public static void main(String[] args) {
        Integer integer = 10;
        int i = integer;
        System.out.println("Integer i = " + i);
        Character ch = 'x';
        char c = ch;
        System.out.println("Character c = " + c);
        Boolean bool = true;
        boolean b = bool;
        System.out.println("Boolean b = " + b);
    }

}
